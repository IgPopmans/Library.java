package Lesson_6;

public class ArrayMinMaxValue {
    public static void main(String[] args) {

        int[] num = {6, 12, 3, 14, 22};

        int min = num[0];
        int max = num[num.length - 1];
        System.out.println("Min Value = " + min);

        System.out.println("Max Value = " + max);


    }
}