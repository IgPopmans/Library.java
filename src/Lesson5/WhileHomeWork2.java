package Lesson5;

public class WhileHomeWork2 {
    //Необходимо вывести на консоль только четные числа от
    // -10(включительно) до числа max(включительно)max=22//

    public static void main(String[] args) {
        int a = -10 ;
        while (a <=  22){

            System.out.print(a+ " ");
            a+=2;
        }
    }

}
